func numcheckfunction() {
  let var1 = 500
  let var2 = 2000
  if var1 > var2 {
    print("Var1 is greater than Var2! \n Var1: \(var1) \n Var2: \(var2)")
  }
  if var2 > var1 {
    print("Var2 is greater than Var1! \n Var1: \(var1) \n Var2: \(var2)")
  }
  if var1 == var2 {
    print("Var1 and Var2 are equal! \n Var1: \(var1) \n Var2: \(var2)")
  }
  
}
numcheckfunction()
